public class Casting {
    public static void main(String args[]) {
        // Type casting
        byte b = 127;
        int a = 12;
        System.out.println(b);
        // System.out.println(a);
        // a = b;
        System.out.println(a);
        b = (byte) a;
        System.out.println(b);

    }
}
