public class InheritanceExample {
    public static void main(String args[]) {
        VeryAdvCalc calObj = new VeryAdvCalc();
        System.out.println(calObj.add(10,23));
        System.out.println(calObj.sub(10,23));
        System.out.println(calObj.mul(10,23));
        System.out.println(calObj.div(23,10));
        System.out.println(calObj.pow(2,8));
    }
}
